import {
  CanActivate,
  ExecutionContext,
  Injectable,
  UnauthorizedException,
} from '@nestjs/common';
import { ConfigService } from '@nestjs/config';
import { JwtService } from '@nestjs/jwt';
import { Request } from 'express';
import { UploadEntity } from '../multer.config';

@Injectable()
export class ConditionalAuthGuard implements CanActivate {
  constructor(
    private jwtService: JwtService,
    private configService: ConfigService,
  ) {}

  async canActivate(context: ExecutionContext): Promise<boolean> {
    const request = context.switchToHttp().getRequest<Request>();
    const entityParam = request.params?.entity?.toLowerCase();

    // Allow /upload/auth without authentication (for owner registration)
    if (entityParam === UploadEntity.AUTH) {
      return true;
    }

    // Require authentication for all other entities
    const token = this.extractTokenFromHeader(request);
    if (!token) {
      throw new UnauthorizedException('errors.auth.token_not_found');
    }

    try {
      const payload = await this.jwtService.verifyAsync(token, {
        secret: this.configService.get<string>('JWT_SECRET'),
      });
      request['user'] = payload;
    } catch {
      throw new UnauthorizedException('errors.auth.invalid_token');
    }

    return true;
  }

  private extractTokenFromHeader(request: Request): string | undefined {
    const [type, token] = request.headers.authorization?.split(' ') ?? [];
    return type === 'Bearer' ? token : undefined;
  }
}
